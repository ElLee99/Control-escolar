    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

           String alumno = " ";
                
                noRegisA = jTextField1.getText();
                nombreA = jTextField2.getText();
                domicilioA = jTextField3.getText();
                telefonoA = jTextField4.getText();
                correoA = jTextField5.getText();
               String calAS = jTextField6.getText();

               try{
                String noMatS = Integer.toString(noAlumnoA);
                calificacionA = Float.parseFloat(calAS);
                if (noRegisA.equals("") || nombreA.equals("null") || domicilioA.equals("null") || telefonoA.equals("null") ||correoA.equals("null") ||noAlumnoA==0 || gradoA.equals("null") || calificacionA==0) {
                   JOptionPane.showMessageDialog(this, "Rellene todos los campos"); 
               }
               else{ 
                alumno = "No. de Registro: " + noRegisA + "\nNombre: " +  nombreA + "\nDomicilio : " + domicilioA + "\nTelefono : " + telefonoA + "\nCorreo: "+ correoA + "\nGrado: " + gradoA + "\nCalificacion: " + calAS + "\nNo. Alumno: " + noMatS;
                JOptionPane.showMessageDialog(this, alumno);  
                dispose();
               } 

                }catch (NumberFormatException error){
                  JOptionPane.showMessageDialog(this, "El caracter que elegiste en Calificacion, no es valido\nIngrese un numero del 1 al 100 y vuelva a intentarlo");
          
      }finally{
      }
    }//GEN-LAST:event_jButton1ActionPerformed

